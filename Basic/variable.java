package Basic;

public class variable {
    public static void main(String[] args) {
        // declare of variable
           int x ;
           // intialization of varriable
           x = 500 ;
           System.out.println(x);
           //updation - changing value
           x = 7;
           System.out.println(x);
           //updation - increment in value
           x = x + 6 ;
           System.out.println(x); 
           // updation - decrement in value
           x =  x - 20;
           System.out.println(x);
    }
}

// 500
// 7
// 13
// -7